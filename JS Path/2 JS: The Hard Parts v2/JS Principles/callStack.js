/*
	Call Stack:

Stack: Data structure that only interact with what is at the top of the stackand when it's finished pops it (with keyword return)

JavaScript keeps track of what function is currently running

Run a function - add to call stack

Finish running the function - JS removes it from call stack

Whatever is top of the call stack -> that's the function we are currently running

At the bottom of the stack it's the global code, when finishes all the functions calls returns to run global
*/


